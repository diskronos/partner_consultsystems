CREATE TABLE `clients` (
	`id` INT(10) UNSIGNED NOT NULL AUTO_INCREMENT,
	`name` VARCHAR(255) NOT NULL,
	`email` VARCHAR(50) NOT NULL,
	`foreign_id` INT(10) UNSIGNED NULL DEFAULT NULL,
	`partner_id` INT(10) UNSIGNED NULL DEFAULT NULL,
	`tariff` VARCHAR(255) NULL DEFAULT NULL,
	`site` VARCHAR(255) NULL DEFAULT NULL,
	`date_expire` INT(10) UNSIGNED NULL DEFAULT NULL,
	`created_at` INT(10) UNSIGNED NOT NULL,
	PRIMARY KEY (`id`)
)
COLLATE='utf8_general_ci'
ENGINE=InnoDB;
